function Uoc = UocCurve(SOC, I)
    % Koefisien polinomial (orde 12 → 0)
    best_coeffs_chg = [ ...
        149824.480167,  ...
       -922763.458129,  ...
        2469980.407773, ...
       -3725981.008787, ...
        3389307.715113, ...
       -1756330.820878, ...
         284822.939917, ...
         266660.895510, ...
        -223248.861376, ...
          83580.597025, ...
         -17852.587658, ...
           2107.293542, ...
           -104.153529  ...
    ];
    best_coeffs_dis = [ ...
       -1446586.834302,  ...
        11163208.973162,  ...
       -38913727.756002,  ...
        80969356.047137,  ...
      -111925290.127096,  ...
       108208342.191703,  ...
       -74975818.999274,  ...
        37490950.914658,  ...
       -13420625.661345,  ...
         3352777.070855,  ...
         -554758.277139,  ...
          54589.548246,  ...
           -2413.754181   ...
    ];

    % Nilai minimum OCV dan ambang SOC dari data
    min_ocv_chg = 2.50909280776978;
    min_ocv_dis = 1.99972391128540;
    min_soc_chg = 0.195212825097076;
    min_soc_dis = 0.240597374751389;

    % Siapkan output
    Uoc = zeros(size(SOC));

    % Indeks mode charging/discharging
    idxC = (I >= 0);
    idxD = (I <  0);

    % 1) Charging: hitung polinomial, lalu clamp
    Uc = polyval(best_coeffs_chg, SOC(idxC));
    % Untuk SOC di bawah ambang, paksa ke nilai minimum
    belowC = SOC(idxC) < min_soc_chg;
    Uc(belowC) = min_ocv_chg;
    Uoc(idxC) = Uc;

    % 2) Discharging: hitung polinomial, lalu clamp
    Ud = polyval(best_coeffs_dis, SOC(idxD));
    belowD = SOC(idxD) < min_soc_dis;
    Ud(belowD) = min_ocv_dis;
    Uoc(idxD) = Ud;
end
